    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow= tblAccounts.getSelectedRow();
        if(selectedRow<0)
            JOptionPane.showMessageDialog(this, "Select a row to proceed");
        
        else
        {
        
        int dialogButton=JOptionPane.YES_NO_OPTION;
        int dialogResult=JOptionPane.showConfirmDialog(this, "Would you like to delete this account?", "Warning", dialogButton);
        if (dialogResult==JOptionPane.YES_OPTION)
        {
            Account account=(Account) tblAccounts.getValueAt(selectedRow, 0);
            accDir.deleteAccount(account);
            populateTable();
        }}
    }//GEN-LAST:event_btnDeleteActionPerformed

