    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        
        
        if(txtAccountNum.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Enter Account number to search the account details", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
        else if (!txtAccountNum.getText().matches("^[0-9]+$"))
        {
            JOptionPane.showMessageDialog(this, "Account number doesn't accept special characters or alphabets. Please enter only numeric value!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else
        {
        Account accDtl=accDir.searchAccount(txtAccountNum.getText());
        if(accDtl==null)
            JOptionPane.showMessageDialog(this, "Account doesn't exist", "Information", JOptionPane.INFORMATION_MESSAGE);
        
        else
        {
            ViewAccountJPanel viewAcc=new ViewAccountJPanel(pnlCard, accDtl, accDir);
            pnlCard.add("ViewAccountJPanel", viewAcc);
            CardLayout cardLayout=(CardLayout) pnlCard.getLayout();
            cardLayout.next(pnlCard);
        }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

