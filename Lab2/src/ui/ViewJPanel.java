    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        selectedRow=tblDisplay.getSelectedRow();
        if (selectedRow<0)
        JOptionPane.showMessageDialog(this, "Please select a row to view");
        
        DefaultTableModel model=(DefaultTableModel)tblDisplay.getModel();
        Vitals v=(Vitals) model.getValueAt(selectedRow, 0); 
        
        txtDate.setText(v.getDate());
        txtBp.setText(String.valueOf(v.getBp()));
        txtPulse.setText(String.valueOf(v.getPulse()));
        txtTemp.setText(String.valueOf(v.getTemp()));
        
        
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        txtTemp.setText("");
        txtBp.setText("");
        txtPulse.setText("");
        txtDate.setText("");
        selectedRow=tblDisplay.getSelectedRow();
        if (selectedRow<0)
            JOptionPane.showMessageDialog(this, "Please select a row to delete");
        
        DefaultTableModel model=(DefaultTableModel)tblDisplay.getModel();
        Vitals v=(Vitals) model.getValueAt(selectedRow, 0);
        vh.deleteVital(v);
        JOptionPane.showMessageDialog(this, "Record deleted successfully");
        populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed


