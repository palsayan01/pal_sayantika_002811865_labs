 public void populateTable()
    {
        DefaultTableModel model=(DefaultTableModel)tblDisplay.getModel();
        model.setRowCount(0);
        for (Vitals v: vh.getVitalSaved())
        {
            Object row[]=new Object[4];
            row[0]=v;
            row[1]=v.getBp();
            row[2]=v.getPulse();
            row[3]=v.getTemp();
            model.addRow(row);
        }
    }


